// Domain — 어체 락 규칙.
// 출력 톤을 Layer 2 본문 어체로 고정. context/instruction 의 어체에 무영향.
// systemPrompt 에 그대로 삽입.

export function toneLockRules() {
  return `**[어체 락 — 절대 규칙]**
- 출력 카피의 어체(존댓말/반말, 종결어미)는 \`<layer2_brand>\` 의 본문 어체를 그대로 따른다.
- Layer 2 의 예문이 \`\`\` 코드 펜스 안에 있어도, 그 안의 어체가 곧 출력 어체다 ("예시" 가 아니라 "이 톤으로 써라" 로 받는다).
- Layer 2 가 존댓말 ("~입니다", "~합니다", "~바라봅니다") 이면 → 출력 모든 문장도 존댓말.
- Layer 2 가 반말·한다체 ("~다", "~된다") 이면 → 출력 모든 문장도 한다체.
- \`<context>\` 의 inputs / instruction 의 어체에 절대 영향받지 않는다. 그 어체가 다르더라도 무시하고 Layer 2 어체로 통일.
- 한 글에서 두 어체가 섞이면 어김. 처음부터 끝까지 한 어체로.`;
}
